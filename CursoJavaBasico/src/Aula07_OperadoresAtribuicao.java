
public class Aula07_OperadoresAtribuicao {

	public static void main(String[] args) {
		int x = 4;
		x += 2; // x = x + 2
		System.out.println(x);
		
		int x2 = 6;
		x2 -= 3; // x2 = x2 - 3
		System.out.println(x2);
		
		int x3 = 8;
		x3 *= 4; // x3 = x3 * 4
		System.out.println(x3);
		
		int x4 = 5;
		x4 /= 5; // x4 = x4 / 5
		System.out.println(x4);
		
		int x5 = 2;
		x5 %= 1; // x5 = x5 % 1
		System.out.println(x5);
	}

}
